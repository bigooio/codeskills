function escapeRegExp(string) {
  return string.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
}

// 处理单个 SKILL.md
function processSkill(skillPath, skillDir) {
  try {
    let content = fs.readFileSync(skillPath, 'utf8')
    const translated = translate(content)
    fs.writeFileSync(skillPath, translated)
    return true
  } catch (e) {
    console.error(`Error processing ${skillDir}:`, e.message)
    return false
  }
}

// 主处理
const dirs = fs.readdirSync(SKILLS_DIR).filter(f => {
  return fs.statSync(path.join(SKILLS_DIR, f)).isDirectory()
})

console.log(`Processing ${dirs.length} skills...\n`)

let processed = 0
let errors = 0

for (const dir of dirs) {
  const skillPath = path.join(SKILLS_DIR, dir, 'SKILL.md')
  if (!fs.existsSync(skillPath)) continue

  if (processSkill(skillPath, dir)) {
    processed++
    if (processed % 20 === 0) {
      console.log(`Translated ${processed}/${dirs.length}...`)
    }
  } else {
    errors++
  }
}

console.log(`\nDone: ${processed} translated, ${errors} errors`)
