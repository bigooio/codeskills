// 纯内容翻译 - 只翻译正文，不翻译 frontmatter
function translate(content) {
  // 分离 frontmatter 和内容
  const match = content.match(/^---\n([\s\S]*?)\n---\n([\s\S]*)$/)
  if (!match) {
    return translateBody(content)
  }

  const frontmatter = match[1]
  const body = match[2]
  return `---\n${frontmatter}\n---\n${translateBody(body)}`
}

